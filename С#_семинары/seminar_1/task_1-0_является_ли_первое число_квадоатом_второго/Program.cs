int numA = int.Parse(Console.ReadLine() ?? "0");
//вводим число в консоли, оно превращается в интеджер и кладётся в переменную numA
//можно и без этой строки про введите второе число
//прога будет работать, в терминале не будет ничего, пустая строка, нужно самому догадаться что сделать
// Console.WriteLine("Введите второе число: ");
int numB = int.Parse(Console.ReadLine() ?? "0");
//вводим число в консоли, оно превращается в интеджер и кладётся в переменную numB


//вторая часть задачи: проверка является ли первое число квадратом второго
if (numB * numB == numA) // одно равно - это присваивание, два равно - это сравнение
{
    Console.WriteLine("Первое число квадрат второго.");
}
else
{
    Console.WriteLine("Второе число не является квадратом первого числа.");
}
