// Напишите программу, которая на вход принимает два числа и проверяет, является ли первое число квадратом второго.
// a = 25, b = 5 -> да
// a = 2, b = 10 -> нет
// a = 9, b = -3 -> да
// a = -3, b = 9 -> нет

//первая часть задачи
//можно и без этой строки про введите первое число
//прога будет работать, в терминале не будет ничего, пустая строка, нужно самому догадаться что сделать
